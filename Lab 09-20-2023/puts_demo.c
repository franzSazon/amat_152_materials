// this program demonstrates the puts() functions

#include <stdio.h>
 
int main(){

    puts("Hello, World!");

    puts("\nHello");
    puts("World!");

    puts("\nHello");
    puts(",");
    puts("World!");

    puts("\nHello\n, \nWorld\n!");

    puts("");
    puts("Hello\t,\tWorld\t!\n");

    puts("  ^~^  ,");
    puts(" ('Y') )");
    puts("  /  \\\" / ");
    puts(" (\"\"|||/)");

    
    return 0;
}